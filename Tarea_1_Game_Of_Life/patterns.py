def create_glider():

    return [

        [0, 1, 0],
        [0, 0, 1],
        [1, 1, 1]

    ]


# -------------------------------------------------


def create_blinker():

    return [

        [0, 0, 0],
        [1, 1, 1],
        [0, 0, 0]

    ]


# -------------------------------------------------


def create_toad():

    return [

        [0, 1, 1, 1],
        [1, 1, 1, 0]

    ]